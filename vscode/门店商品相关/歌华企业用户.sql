--用户周均购买次数
select
t1.user_phone_ming --用户手机号
,count(distinct order_no) as order_no_num --订单数量
,sum(payable_price) as payable_price --消费金额
from data_promotion.dm_promotion_store_detl_order_detail_info_da t
left join data_build.ods_uploads_one_hundred_and_fifty t1 on t.pay_id = t1.user_id
where t.dt = 20240331
and t.order_status = 'FINISHED'
and t.store_type = '0'
and t.sku_class_code not in ('50','86')
and t.order_date between '2023-06-01' and '2024-03-31'
and t.store_code in ('100078005')
and t.pay_id is not null
and t.sku_quantity > 0
group by
t1.user_phone_ming

