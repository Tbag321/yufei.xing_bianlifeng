--中分类商品库存金额
select
t1.record_date
,t1.store_code
,t1.store_name
,t1.sku_division_code
,sum(sku_inventory_price) as division_inventory_price
from(
select
t.record_date
,t.store_code
,t.store_name
,t.sku_division_code
,t.sku_code
,t.sku_name
,(t.cost_price + t.cost_tax) * sku_inventory_beginning as sku_inventory_price --商品含税成本
from(
select
t.record_date
,t.store_code
,t.store_name
,t.sku_division_code
,t.sku_code
,t.sku_name
,t1.cost_price --去税成本价
,t1.cost_tax --成本税金
,sum(t.sku_inventory_beginning) as sku_inventory_beginning --期初库存
from data_smartorder.app_inventory_store_sku_di t
left join default.mid_inventory_price_bach_v1 t1 on t.store_code = t1.store_code and t.record_date = date_format(t1.valid_start_time,"yyyy-MM-dd") and t.sku_code = t1.sku_code and t1.dt = 20240428
where t.dt in (20230831,20230930,20231031,20231130,20231231,20240131,20240229,20240331)
and t.store_code = '100078005'
and store_type = '0'
group by
t.record_date
,t.store_code
,t.store_name
,t.sku_division_code
,t.sku_code
,t.sku_name
,t1.cost_price --去税成本价
,t1.cost_tax --成本税金
) t
) t1
group by
t1.record_date
,t1.store_code
,t1.store_name
,t1.sku_division_code